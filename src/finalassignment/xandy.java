package finalassignment;

public class xandy {
	
	private long T;
	private long x;
	private long y;
	public xandy(long t, long x, long y) {
		super();
		T = t;
		this.x = x;
		this.y = y;
	}
	public long getT() {
		return T;
	}
	public void setT(long t) {
		T = t;
	}
	public long getX() {
		return x;
	}
	public void setX(long x) {
		this.x = x;
	}
	public long getY() {
		return y;
	}
	public void setY(long y) {
		this.y = y;
	}

}
